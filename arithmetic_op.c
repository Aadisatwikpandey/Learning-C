#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>

int main()
{
	int a, b;
    float c, d;
    
    
    scanf("%d[64]", &a);
    
  
    scanf("%d[64]", &b);
    
    
    scanf("%f[64]", &c);
    
    
    scanf("%f[64]", &d);
    
    
    
    printf("%d %d \n", a+b, a-b );
    printf("%3.1f %3.1f", c+d, c-d);
    
    return 0;
}
